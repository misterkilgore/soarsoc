#!/bin/bash

echo "=============================="
echo "  Git Cheatsheet Essenziale"
echo "=============================="
echo

echo "1. Inizializzare un repository Git locale:"
echo "   git init"
echo

echo "2. Controllare lo stato dei file:"
echo "   git status"
echo

echo "3. Aggiungere file all'area di staging:"
echo "   git add <file>"
echo "   git add .   # aggiunge tutti i file modificati"
echo

echo "4. Fare un commit:"
echo "   git commit -m \"Messaggio del commit\""
echo

echo "5. Visualizzare la cronologia dei commit:"
echo "   git log"
echo

echo "6. Configurare una repository remota (es. GitHub):"
echo "   git remote add origin <URL_del_repository_remoto>"
echo

echo "7. Verificare le repository remote configurate:"
echo "   git remote -v"
echo

echo "8. Inviare i commit locali al repository remoto:"
echo "   git push -u origin main"
echo "   # Nota: sostituisci 'main' con il nome del tuo branch principale"
echo

echo "9. Clonare una repository remota:"
echo "   git clone <URL_del_repository_remoto>"
echo

echo "10. Recuperare aggiornamenti dal repository remoto:"
echo "    git pull"
echo

echo "------------------------------"
echo "Configurazione utente Git (necessaria almeno una volta):"
